/**
 * <h1>Node Class</h1>
 * Node models a node object used for a Binary Tree
 * <p>
 * This Node object is limited to holding a single piece of data, and references to
 * its left and right children
 *
 * @author John Berkley
 *         CPP Class: CS 241
 *         Date Created: Apr 23, 2017
 */
public class Node {
    /**Contains the int value stored in Node*/
    public int data;
    /**Contains a reference to the left child of the current node*/
    public Node leftChild;
    /**Contains a reference to the right child of the current node*/
    public Node rightChild;
}
